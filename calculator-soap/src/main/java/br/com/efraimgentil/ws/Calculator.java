package br.com.efraimgentil.ws;

public class Calculator {
	
	public double sum(double i , double j){
		return i + j;
	}
	
	public double minus(double i , double j){
		return i - j;
	}
	
	public double plus(double i , double j){
		return i * j;
	}
	
	public double divide(double i, double j){
		return i / j;
	}
	
}